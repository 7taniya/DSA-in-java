package Java_basic_Programs;

public class Literals_in_Java {
    public static void main(String[] args) {
        byte age = 18; // here 18 is constant and in Java it is known as Integer Literal.
        char ch = 'A'; // Char literal should be in single quotes.
        float f = 5.6f; // Mention f after the literal because by default it is double.
        double d = 4.66; // it is by default double.
        long l = 373873340403L; // by default it is int we have to put l or L at last for making it long.
        String str = "Akash Deep Vishwakarma"; // for string type use double quotes.
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f);
        System.out.println(d);
        System.out.println(l);
        System.out.print(str);
    }
}
